const mongoose = require("mongoose");
const bcrypt = require("bcrypt");

const userModuleSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  mobileNumber: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    select: false,
    minlength: [6, "password must be a six character"],
  },
  status: {
    type: String,
    required: true,
  },
});

userModuleSchema.pre("save", async function (next) {
  if (!this.isModified("password")) {
    return next();
  }
  try {
    const salt = await bcrypt.genSalt(10);
    this.password = await bcrypt.hash(this.password, salt);
    next();
  } catch (error) {
    next(error);
  }
});

const userModule = mongoose.model("user", userModuleSchema);

module.exports = userModule;
